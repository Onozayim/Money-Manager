                <div class="mb-4 border p-3 rounded">
                    <h3 class="text-secondary">3.1 La Importancia del Ahorro</h3>
                    <p>El ahorro es un pilar fundamental de la salud financiera. Este hábito permite a
                        las personas crear un fondo de emergencia, cumplir objetivos financieros y
                        enfrentar imprevistos sin necesidad de recurrir a deudas.</p>
                    <p><strong>Principales beneficios del ahorro:</strong></p>
                    <ul>
                        <li><strong>Fondo de emergencia:</strong> Protegerse contra imprevistos como
                            pérdida de empleo, gastos médicos o reparaciones importantes.</li>
                        <li><strong>Objetivos a corto plazo:</strong> Lograr metas personales como unas
