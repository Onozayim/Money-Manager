                <div class="mb-4 border p-3 rounded">
                    <h3 class="text-secondary">3.2 Diferencias entre Ahorro e Inversión</h3>
                    <p>Es esencial entender que el ahorro y la inversión, aunque relacionados, cumplen
                        funciones diferentes en la planificación financiera.</p>
                    <p><strong>Ahorro:</strong> Se refiere a la reserva de dinero en cuentas de fácil
                        acceso, generalmente con bajo o nulo interés. Es seguro y líquido, ideal para
                        cubrir emergencias o gastos a corto plazo.</p>
                    <p><strong>Inversión:</strong> Implica el uso del dinero en instrumentos que generen
                        rendimientos a cambio de asumir ciertos riesgos. Las inversiones están
                        orientadas a objetivos a mediano y largo plazo.</p>
                    <p>En general, el ahorro es el primer paso para construir una base financiera
                        sólida, mientras que la inversión permite que el dinero trabaje y genere
                        ingresos adicionales.</p>
                </div>

                <div class="mb-4 border p-3 rounded">
                    <h3 class="text-secondary">3.3 Opciones de Inversión</h3>
                    <p>Existen diversas opciones de inversión, cada una con niveles de riesgo, liquidez
                        y rendimiento diferentes. Es importante conocer estas características para
                        elegir la opción que mejor se ajuste a los objetivos financieros personales.</p>
                    <p><strong>Principales opciones de inversión:</strong></p>
                    <ul>
                        <li><strong>Cuentas de ahorro y certificados de depósito (CD):</strong> Ofrecen
                            rendimientos bajos pero son seguros y de fácil acceso. Ideales para
                            inversionistas conservadores.</li>
                        <li><strong>Fondos de inversión:</strong> Reúnen dinero de varios inversionistas
                            para adquirir una cartera diversificada. Son administrados por profesionales
                            y pueden incluir acciones, bonos y otros activos.</li>
                        <li><strong>Acciones:</strong> Comprar acciones de una empresa permite
                            participar en su crecimiento. Aunque puede ofrecer altos rendimientos,
                            también implica mayor volatilidad y riesgo.</li>
                        <li><strong>Bonos:</strong> Los bonos son préstamos que el inversionista hace a
                            una entidad (como un gobierno o corporación) a cambio de intereses. Su
                            riesgo es generalmente bajo y ofrecen ingresos fijos.</li>
                        <li><strong>Bienes raíces:</strong> Invertir en propiedades puede ofrecer
                            ingresos pasivos y apreciación del capital. Sin embargo, requiere una
                            inversión inicial alta y tiene baja liquidez.</li>
                        <li><strong>Criptomonedas:</strong> Son monedas digitales con gran volatilidad y
                            alto riesgo. Son atractivas por sus potenciales altos rendimientos, pero
                            también pueden llevar a pérdidas significativas.</li>
                    </ul>
                    <p>La diversificación entre diferentes tipos de inversión permite equilibrar el
                        riesgo y maximizar los rendimientos.</p>
                </div>

                <div class="mb-4 border p-3 rounded">
                    <h3 class="text-secondary">3.4 Estrategias de Inversión</h3>
                    <p>Las estrategias de inversión ayudan a los inversionistas a organizar su capital y
                        tomar decisiones basadas en sus objetivos y tolerancia al riesgo. Aquí algunos
                        enfoques comunes:</p>
                    <ul>
                        <li><strong>Inversión a largo plazo:</strong> Mantener las inversiones por un
                            periodo extenso, aprovechando el crecimiento sostenido y reduciendo el
                            impacto de la volatilidad a corto plazo.</li>
                        <li><strong>Inversión de crecimiento:</strong> Buscar activos que tienen el
                            potencial de aumentar significativamente su valor, como acciones de
                            tecnología o empresas emergentes.</li>
                        <li><strong>Inversión en valor:</strong> Comprar activos infravalorados con el
                            objetivo de beneficiarse cuando el mercado los reconozca y suba su valor.
                        </li>
                        <li><strong>Dividendos:</strong> Invertir en acciones de empresas que
                            distribuyen parte de sus ganancias a los accionistas regularmente, generando
                            ingresos pasivos.</li>
                    </ul>
                    <p>Es importante ajustar la estrategia con regularidad y considerar factores como
                        cambios en el mercado, metas financieras y situación personal.</p>
